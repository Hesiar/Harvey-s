    <h1>Preguntas Frecuentes</h1>
    <h2>¿Cómo puedo hacer un pedido?</h2>
    <p>Para hacer un pedido, simplemente navega por nuestro sitio web, selecciona los productos que deseas y agrégales al carrito. Luego, sigue el proceso de pago para completar tu compra.</p>
    <h2>¿Cuáles son los métodos de pago aceptados?</h2>
    <p class="tarjetas">Aceptamos tarjetas de crédito, débito y prepago como métodos de pago. Aceptamos tarjetas Visa, Mastercard, American Express y Discover.
        <br><img src="../elementos/pics/visa-mastercard-discover-american-express-icons.png" alt="Tarjetas aceptadas">
    </p>
    <h2>¿Puedo devolver un producto?</h2>
    <p>Sí, puedes devolver un producto dentro de los 30 días posteriores a la compra, siempre y cuando se asegure de que esté en su estado original y con el recibo de compra.
        Si se tratase de un producto congelado o refrigerado, no se aceptará la devolución.
    </p>
    <h2>¿Ofrecen envío internacional?</h2>
    <p>Actualmente no.</p>
    <h2>¿Cómo puedo contactar al servicio al cliente?</h2>
    <p>Puedes contactar a nuestro servicio al cliente llamando al número de atención al cliente que aparece en la parte inferior de la página. Nuestros empleados estaran encantado de ayudarle en lo que necesite.</p>
    <h2>¿Tienen una tienda física?</h2>
    <p>Sí, tenemos una tienda física ubicada en Calle de la Inventación 31, Granada, España. Puedes visitarnos durante nuestro horario de apertura al público.</p>
    <h2>¿Ofrecen descuentos por compras al por mayor?</h2>
    <p>No, Harvey's se dedica exclusivamente a la venta al por menor.</p>
    <h2>¿Tienes alguna otra pregunta?</h2>
    <p>Si tienes alguna otra pregunta, no dudes en ponerte en contacto con nuestro teléfono de atención al cliene. Estamos aquí para ayudarte.</p>
